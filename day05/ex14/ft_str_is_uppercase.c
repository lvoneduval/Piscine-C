int		ft_str_is_uppercase(char *str)
{
	int i;
	int is_up;

	i = 0;
	is_up = 1;
	while (str[i] && is_up)
	{
		if ((str[i] < 65) || (str[i] > 90))
			is_up = 0;
		i++;
	}
	return (is_up);
}
