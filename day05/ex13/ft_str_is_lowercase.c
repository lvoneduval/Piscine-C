int		ft_str_is_lowercase(char *str)
{
	int i;
	int is_low;

	i = 0;
	is_low = 1;
	while (str[i] && is_low)
	{
		if ((str[i] < 97) || (str[i] > 122))
			is_low = 0;
		i++;
	}
	return (is_low);
}
